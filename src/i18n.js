// t(key, lang) — terjemahkan key ke bahasa
export function t(key, lang = 'id') {
  const str = STRINGS[lang]?.[key] ?? STRINGS.id?.[key] ?? key;
  return str;
}

// Shorthand: bind lang ke t
export function makeT(lang) {
  return (key, vars = {}) => {
    let str = t(key, lang);
    // Simple variable interpolation: {var}
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
    return str;
  };
}
